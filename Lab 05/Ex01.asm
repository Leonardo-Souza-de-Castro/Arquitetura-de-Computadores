	ORG 0000H
	LJMP INICIO

	ORG 0100H
INICIO:

ENDER_UM EQU 0AH
ENDER_DOIS EQU 00H

	MOV R0, #ENDER_UM
	MOV R1, #ENDER_DOIS
	ACALL Main
	SJMP $

Main:
	MOV A, @R0
	SUBB A, @R1
	
	JZ IGUAL
	JC R_ZERO_MENOR
	JNC R_UM_MENOR
IGUAL:
	RET

R_ZERO_MENOR:
	MOV A, @R0
	MOV R5, A
	RET

R_UM_MENOR:
	MOV A, @R1
	MOV R5, A
	RET