LOOP_COMPARACAO:
    MOV A, @R0
    ACALL Main
    INC R0
    CJNE R0, #ENDER_DOIS, LOOP_COMPARACAO
    MOV 29H, R5
    MOV P1, R6
    SJMP $

Main:
	MOV B, R5
	CLR C
	SUBB A, B
	
	JZ IGUAL
	JC ATUAL_MAIOR
	JNC NOVO_MAIOR

IGUAL:
	INC R6
	RET

ATUAL_MAIOR:
	RET

NOVO_MAIOR:
	MOV B, @R0
	MOV R5, B
	MOV R6, #00H
	INC R6
	RET





